package dictionary;

/**
 * this class is used for to make pair of key & value
 * @author yash.porwal_metacube
 *
 */
public class Pair {
	String key;
	String value;
	
	/**
	 * constructor which takes key & value and make the pair 
	 * @param key as a String
	 * @param value as a Value
	 */
	Pair(String key,String value){
		this.key = key;
		this.value = value;
	}
}